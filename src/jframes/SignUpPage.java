    public boolean valEmail(){
        String email=Email.getText();
        String pattern="^[a-zA-Z0-9._]+\\.ies+@ipsacademy\\.org$";
        Pattern pat=Pattern.compile(pattern);
        Matcher Match=pat.matcher(email);
        return Match.matches();
    }
    public boolean valContact(){
        String contact=Contact.getText();
        String pattern="^[0-9]{10}$";
        Pattern pat=Pattern.compile(pattern);
        Matcher Match=pat.matcher(contact);
        return Match.matches();
        
    }
    
    //Validating everything
    
    public boolean ValidateSignup(){
        
        String Uname=Username.getText();
        String password=Password.getText();
        String email=Email.getText();
        String contact=Contact.getText();
       
        if (Uname.equals("")){
            JOptionPane.showMessageDialog(this,"Please enter a valid username");
            return false;
        }
        
        if (password.equals("")){
            JOptionPane.showMessageDialog(this,"Please enter a password");
            return false;
        }
        
        if (email.equals("") || valEmail()==false){
            JOptionPane.showMessageDialog(this, "Invalid email please enter a valid email address");
            return false;
        }
        if (contact.equals("") || valContact()==false){
            JOptionPane.showMessageDialog(this, "Invalid Contact please enter a valid contact");
            return false;
        }
        return true;
    }
