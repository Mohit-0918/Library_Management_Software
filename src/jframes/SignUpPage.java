    //method to insert values into Users table
    public void insertSignupDetails(){
        String Uname=Username.getText();
        String password=Password.getText();
        String email=Email.getText();
        String contact=Contact.getText();
        
        try{
            Connection con = DBConnection.getConnection();
            String sql;
            sql = "insert into Users(name,email,contact,password) values (?,?,?,?)";
            PreparedStatement pst =con.prepareStatement(sql);
            
            pst.setString(1, Uname);
            pst.setString(4, password);
            pst.setString(2, email);
            pst.setString(3, contact);
            
            int updatedRowCount =pst.executeUpdate();
            if (updatedRowCount>0){
                JOptionPane.showMessageDialog(this,"Recorded Inserted Successfully");
                LoginPage login=new LoginPage();
                login.setVisible(true);
                this.dispose();
            }
                else
                JOptionPane.showMessageDialog(this, "Recorde Insertion Failed");
            

        }catch (Exception e){
            e.printStackTrace();
        }
    }
